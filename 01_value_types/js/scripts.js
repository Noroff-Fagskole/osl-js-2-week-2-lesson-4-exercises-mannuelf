// IN JS we have two kind of Types

// VALUE TYPES and REFERENCE TYPES

/*

  VALUE TYPES aka Primitives
  - Number
  - String
  - Boolean
  - Symbol
  - undefined
  - null

  REFERENCE TYPES
  - Objects
  - Function
  - Array


  They behave differently to each other
  - Primitives are copied by their value
  - Objects are copied by their reference

*/
